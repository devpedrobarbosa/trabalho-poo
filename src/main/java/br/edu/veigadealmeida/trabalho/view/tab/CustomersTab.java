    public CustomersTab(Employee employee) {
        initComponents();
        partnerManager = new CustomerManager(partnerDatabase);
        employeeManager = new EmployeeManager(employeeDatabase);
        columns = new String[]{"Nome", "Documento", "Endereço", "Telefone", "E-Mail", "Representante", "Responsável", "Login", "Senha"};
        table.setModel(new DefaultTableModel(partnerManager.toDataArray(), columns));
        Enumeration<TableColumn> tableColumns = table.getColumnModel().getColumns();
        while(tableColumns.hasMoreElements())
            tableColumns.nextElement().setMinWidth(150);
        table.setDefaultEditor(Object.class, null);
        filterSelector.setModel(new DefaultComboBoxModel<>(columns));
        columnFilters = Map.of(
                "Nome", (p, s) -> p.getName().toLowerCase().startsWith(s.toLowerCase()),
                "Documento", (p, s) -> p.getDocument().startsWith(s),
                "Endereço", (p, s) -> p.getAddress().toLowerCase().startsWith(s.toLowerCase()),
                "Telefone", (p, s) -> p.getPhone().startsWith(s),
                "E-Mail", (p, s) -> p.getEmail().toLowerCase().startsWith(s.toLowerCase()),
                "Representante", (p, s) -> p.getRepresentative().toLowerCase().startsWith(s.toLowerCase()),
                "Responsável", (p, s) -> p.getResponsibleEmployee().toLowerCase().startsWith(s.toLowerCase()),
                "Login", (p, s) -> p.getLogin().equals(s),
                "Senha", (p, s) -> p.getPassword().equals(s)
        );
        this.employee = employee;
        if(employee.getDepartment().ordinal() < Department.PMO.ordinal()) {
            addButton.setEnabled(false);
            editButton.setEnabled(false);
            deleteButton.setEnabled(false);
        }
    }

