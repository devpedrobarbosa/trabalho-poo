    private void addEmployeeClick(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addEmployeeClick
        // TODO add your handling code here:
        if(employeeManager.getAllTypes().isEmpty()) {
            Util.showError(this, "Nenhum funcionário cadastrado para ser responsável pelo associado.");
            return;
        }
        new AddCustomerView(employee, partnerManager, employeeManager).setVisible(true);
    }//GEN-LAST:event_addEmployeeClick

    
    private void deleteButtonClick(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonClick
        // TODO add your handling code here:
        if(partnerManager.getAllTypes().isEmpty()) {
            Util.showError(this, "Nenhum associado cadastrado no sistema.");
            return;
        }
        new DeleteCustomerView(partnerManager).setVisible(true);
    }//GEN-LAST:event_deleteButtonClick

    private void editButtonClick(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonClick
        // TODO add your handling code here:
        if(table.getSelectedRowCount() != 1) {
            Util.showError(this, "Selecione 1 associado para editar.");
            return;
        }
        String name = table.getValueAt(table.getSelectedRow(), 0).toString();
        Customer partner = partnerManager.getType(name);
        if(partner == null) {
            Util.showError(this, "Não foi possível editar o associado.");
            return;
        }
        new EditCustomerView(partnerManager, employeeManager, partner).setVisible(true);
    }//GEN-LAST:event_editButtonClick

    private void filterTable() {
        Main.Func2<Customer, String, Boolean> func = columnFilters.get(filterSelector.getItemAt(filterSelector.getSelectedIndex()));
        if(func == null) return;
        String query = queryField.getText();
        table.setModel(new DefaultTableModel(partnerManager.toDataArray(partnerManager.getAllTypes().stream().filter(partner -> func.make(partner, query)).toList()), columns));
        revalidate();
    }
    
