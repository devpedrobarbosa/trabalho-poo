    private void deleteClick(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteClick
        // TODO add your handling code here:
        Customer partner = manager.getType(partnerSelector.getItemAt(partnerSelector.getSelectedIndex()));
        if(partner == null) {
            Util.showError(this, "Associado n√£o encontrado.");
            return;
        }
        manager.getAllTypes().remove(partner);
        manager.getDatabase().save(manager.getAllTypes());
        dispose();
        AdminAppView.requestUpdateVisualization();
    }//GEN-LAST:event_deleteClick

